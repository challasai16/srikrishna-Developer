                <h5>Is permission required from RBI to acquire/purchase immovable property in India? </h5> <span><i class="bi bi-plus-lg"></i></span>
                </div>
                <div class="nri-faq-content">
                    <p>No. General Permission is available to purchase only a residential/commercial property in India to a person resident outside India who is a citizen of India (NRI) and who is a Person of Indian Origin (PIO).</p>
                </div>
            </div>
            <!-- 3 -->
            <div class="nri-faq-box">
                <div class="nri-heading s-flex">
