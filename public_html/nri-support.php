                </div>
                <div class="nri-faq-content">
                    <p>The purchase consideration should be met either out of inward remittances in foreign exchange through normal banking channels or out of funds from NRE / FCNR accounts maintained with banks in India.They are required to file a declaration in form IPI 7 with the Central Office of Reserve Bank at Mumbai within a period of 90 days from the date of purchase of immovable property or final payment of purchase consideration along with a certified copy of the document evidencing the transactions and bank certificate regarding the consideration paid.</p>
                </div>
            </div>
            <!-- 4 -->
            <div class="nri-faq-box">
                <div class="nri-heading s-flex">
                <h5>Can such property be sold without permission of Reserve Bank? </h5> <span><i class="bi bi-plus-lg"></i></span>
                </div>
                <div class="nri-faq-content">
                    <p>Yes, Reserve Bank has granted general permission for sale of such property. However, whether the property is purchased by another foreign citizen of Indian Origin, funds towards the purchase consideration should either be remitted to India or paid out of balance in NRE / FCNR accounts.</p>
                </div>
            </div>
            
            <!-- 5 -->
            <div class="nri-faq-box">
                <div class="nri-heading s-flex">
                <h5>Can sale proceeds of such property be remitted out of India? </h5> <span><i class="bi bi-plus-lg"></i></span>
                </div>
                <div class="nri-faq-content">
                    <p>With respect to residential properties purchased on or after 26th May 1993, Reserve Bank considers applications for repatriation of sale proceeds up to the consideration amount remitted in foreign exchange for the acquisition of the property for two such properties. The balance amount of sale proceeds if any or sale proceeds in respect of properties purchased prior to 26th May 1993 will have to be credited to the ordinary non-resident rupee account of the owner of the property.</p>
                </div>
            </div>
            <!-- 6 -->
