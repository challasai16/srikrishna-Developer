                </div>
                <div class="nri-faq-content">
                    <p>Applications for necessary permission for remittance of sale proceeds should be made in form IPI 8 to the Central Office of Reserve Bank at Mumbai within 90 days of the sale of the property. Applications for repatriation of sale proceeds will be considered, provided the sale takes place after three years from the date of final purchase deed from the date of payment of final installment of consideration amount, whichever is later.</p>
