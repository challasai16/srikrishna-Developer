                <h5>Who Is Non- Resident Indian (NRI) and Person of Indian Origin (PIO)? </h5> <span><i class="bi bi-plus-lg"></i></span>
                </div>
                <div class="nri-faq-content">
                    <p>An Indian Citizen who stays abroad for employment/carrying on business or vacation outside India or stays abroad under circumstances indicating an intention for an uncertain duration of stay abroad is a non-resident. (Persons posted in U. N. Organizations and officials deputed abroad by Central/state Government and Public Sector undertakings on temporary assignments are also treated as non-residents). Non-resident foreign citizen of Indian Origin are treated on par with non-residential Indian Citizen (NRIs) for the purpose of certain facilities.</p>
                    <p>A foreign citizen (other than a citizen of Pakistan, Bangladesh, Afghanistan, Bhutan, Sri Lanka or Nepal), is deemed to be of the Indian Origin if he held and Indian Passport at any time, or he or his father or parental grand-father was a citizen of India by virtue of the (Constitution of India or the Citizenship Act, 1955(57 of 1955).</p>
                </div>
            </div>
